package basic.mahwish;

public class DataVariables {

	public static void main(String[] args) {
		
		//Primitive Data Types:- 8types including integer, float/decimal, boolean, char, short, long, byte, double
		int a = 2;
		double b = 12.7;
		char c = 'C';
		boolean d = true;
		
		
		//Non Primitive Data Types:-  including String, Arrays, Interfaces, etc.
		
		String name = "Java";
		System.out.println(name);

		//Declare a variable
		int day;
		
		
		//initialize a variable
		day = 10;
		
		//declare and initialize together
		int dis = 2;
		
		System.out.println(day);
	}

}
